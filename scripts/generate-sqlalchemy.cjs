#!/usr/bin/env node
/**
 * generate-sqlalchemy.cjs — SQLAlchemy 2 async ORM model emission.
 *
 * Walks schemas/*.schema.json. Schemas carrying `x-postgresql-skip: true` are
 * skipped (response envelopes, value objects, etc.). For the rest, emits a
 * minimal async ORM class under services/api/app/models/generated/<entity>.py.
 *
 * W1 minimal emitter — the W2 rewrite (Track B) will replace this with the
 * real sqlacodegen + JSON-Schema → SQLAlchemy bridge. At W1 we just emit a
 * stub class so the codegen contract is provably end-to-end.
 */
'use strict'

const fs = require('node:fs')
const path = require('node:path')

const ROOT = path.resolve(__dirname, '..')
const SCHEMAS_DIR = path.join(ROOT, 'schemas')
const OUT_DIR = path.join(ROOT, 'services', 'api', 'app', 'models', 'generated')

fs.mkdirSync(OUT_DIR, { recursive: true })

const schemas = fs.readdirSync(SCHEMAS_DIR).filter((f) => f.endsWith('.schema.json'))

let emitted = 0
for (const file of schemas) {
  const json = JSON.parse(fs.readFileSync(path.join(SCHEMAS_DIR, file), 'utf8'))
  if (json['x-postgresql-skip']) continue

  const stem = file.replace(/\.schema\.json$/, '').replace(/-/g, '_')
  const ClassName = (json.title || stem).replace(/[^A-Za-z0-9]/g, '')
  const tableName = stem + 's' // plural per naming-conventions
  const outPath = path.join(OUT_DIR, `${stem}.py`)

  const body = `# Generated by scripts/generate-sqlalchemy.cjs — do not edit.
from __future__ import annotations

from datetime import datetime
from uuid import UUID, uuid4

from sqlalchemy import DateTime, String
from sqlalchemy.dialects.postgresql import JSONB, UUID as PgUUID
from sqlalchemy.orm import Mapped, mapped_column

from app.models.base import Base


class ${ClassName}(Base):
    __tablename__ = "${tableName}"

    id: Mapped[UUID] = mapped_column(PgUUID(as_uuid=True), primary_key=True, default=uuid4)
    created_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), nullable=False)
    updated_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), nullable=False)
    metadata_: Mapped[dict | None] = mapped_column("metadata", JSONB, nullable=True)

    # NOTE: W1 stub. Full column projection lands W2 (Track B).
`
  fs.writeFileSync(outPath, body)
  console.log(`  ${file} → ${path.relative(ROOT, outPath)}`)
  emitted++
}

const initPath = path.join(OUT_DIR, '__init__.py')
fs.writeFileSync(initPath, '# Generated by scripts/generate-sqlalchemy.cjs — do not edit.\n')

console.log(`[sqlalchemy] OK (${emitted} model(s)).`)
