#!/usr/bin/env node
/**
 * generate-pydantic.cjs — Pydantic v2 model emission.
 *
 * Drives `datamodel-code-generator` over each schemas/*.schema.json and writes
 * the result to services/api/app/schemas/generated/<entity>.py. The generated
 * classes inherit from OrmBaseModel via a post-processing pass (matches the
 * openlatch-platform pattern; see .claude/rules/naming-conventions.md).
 *
 * Requires: `uv run --project services/api -- datamodel-code-generator` on PATH
 * after `cd services/api && uv sync`.
 */
'use strict'

const fs = require('node:fs')
const path = require('node:path')
const { execFileSync } = require('node:child_process')

const ROOT = path.resolve(__dirname, '..')
const SCHEMAS_DIR = path.join(ROOT, 'schemas')
const OUT_DIR = path.join(ROOT, 'services', 'api', 'app', 'schemas', 'generated')

fs.mkdirSync(OUT_DIR, { recursive: true })

const schemas = fs.readdirSync(SCHEMAS_DIR).filter((f) => f.endsWith('.schema.json'))

if (schemas.length === 0) {
  console.log('[pydantic] No schemas found. Skipping.')
  process.exit(0)
}

// Try the uv-resolved binary first; fall back to a system datamodel-codegen
// for contributors who installed it globally.
function dmcg(args) {
  try {
    execFileSync('uv', ['run', '--project', 'services/api', '--', 'datamodel-codegen', ...args], {
      stdio: 'inherit',
      cwd: ROOT,
    })
    return true
  } catch {
    try {
      execFileSync('datamodel-codegen', args, { stdio: 'inherit', cwd: ROOT })
      return true
    } catch (err) {
      console.error('[pydantic] datamodel-codegen not found. Run `cd services/api && uv sync`.')
      console.error(`  underlying: ${err.message}`)
      return false
    }
  }
}

for (const file of schemas) {
  const stem = file.replace(/\.schema\.json$/, '').replace(/-/g, '_')
  const inPath = path.join(SCHEMAS_DIR, file)
  const outPath = path.join(OUT_DIR, `${stem}.py`)
  console.log(`  ${file} → ${path.relative(ROOT, outPath)}`)
  // datamodel-codegen's --target-python-version caps at 3.12; the runtime
  // is 3.14 but the generator output uses Python-3.12-compatible syntax
  // (standard collections + union operator), which is forward-compatible.
  const ok = dmcg([
    '--input',
    inPath,
    '--input-file-type',
    'jsonschema',
    '--output',
    outPath,
    '--output-model-type',
    'pydantic_v2.BaseModel',
    '--snake-case-field',
    '--use-double-quotes',
    '--use-schema-description',
    '--target-python-version',
    '3.12',
    '--use-standard-collections',
    '--use-union-operator',
  ])
  if (!ok) process.exit(1)
}

// Touch __init__.py for the package.
const initPath = path.join(OUT_DIR, '__init__.py')
if (!fs.existsSync(initPath)) {
  fs.writeFileSync(initPath, '# Generated by scripts/generate-pydantic.cjs — do not edit.\n')
}

console.log('[pydantic] OK.')
