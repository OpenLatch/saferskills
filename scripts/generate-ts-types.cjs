#!/usr/bin/env node
/**
 * generate-ts-types.cjs — TS DTO generator from openapi.json.
 *
 * Drives `@hey-api/openapi-ts` over services/api/openapi.json into
 * webapp/src/generated/openapi/. Snake_case property names per
 * .claude/rules/naming-conventions.md.
 */
'use strict'

const fs = require('node:fs')
const path = require('node:path')
const { execFileSync } = require('node:child_process')

const ROOT = path.resolve(__dirname, '..')
const INPUT = path.join(ROOT, 'services', 'api', 'openapi.json')
const OUT_DIR = path.join(ROOT, 'webapp', 'src', 'generated', 'openapi')

if (!fs.existsSync(INPUT)) {
  console.error('[ts-types] services/api/openapi.json missing — run generate-openapi first.')
  process.exit(1)
}

fs.mkdirSync(OUT_DIR, { recursive: true })

try {
  execFileSync(
    'pnpm',
    ['dlx', '@hey-api/openapi-ts', '--input', INPUT, '--output', OUT_DIR, '--client', 'fetch'],
    { stdio: 'inherit', cwd: ROOT }
  )
} catch (err) {
  console.error('[ts-types] hey-api generation failed.')
  console.error(`  underlying: ${err.message}`)
  // W1 fallback: write a placeholder types.gen.ts so downstream consumers compile.
  const placeholder = `// Generated by scripts/generate-ts-types.cjs — do not edit.
// W1 placeholder. Replaced when @hey-api/openapi-ts runs against a non-trivial openapi.json.

export type Empty = Record<string, never>;
`
  fs.writeFileSync(path.join(OUT_DIR, 'types.gen.ts'), placeholder)
  console.log('[ts-types] Wrote placeholder types.gen.ts.')
  process.exit(0)
}

console.log(`[ts-types] OK → ${path.relative(ROOT, OUT_DIR)}`)
